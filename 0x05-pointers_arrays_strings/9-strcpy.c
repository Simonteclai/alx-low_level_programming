/**
 * _strcpy - copies a string from str to dest including null
 * @src: the string to be copied
 * @dest: buffer place
 * Return: dest pointer
 */
char *_strcpy(char *dest, const char *src)
{
	int i = 0;

	src[i];

	while (src[i++])
	{
		strcpy(src, dest);
	}
	return (dest);
}

